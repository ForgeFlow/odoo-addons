import logging
import werkzeug

import openerp
from openerp.addons.auth_signup.res_users import SignupError
from openerp.addons.web.controllers.main import ensure_db
from openerp import http
from openerp.http import request
from openerp.tools.translate import _

_logger = logging.getLogger(__name__)


class auth_request(openerp.addons.web.controllers.main.Home):

    @http.route('/web/requestaccount', type='http', auth='public', website=True)
    def request_account(self, *args, **kw):

        subject = _('Request an account')
        return request.redirect('/page/contactus?name=%s' % subject)




